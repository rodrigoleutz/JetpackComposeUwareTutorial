package br.com.uware.jetpackcomposeuwaretutorial.data.utils


infix fun Float.lessThan(num: Float): Boolean = this < num
infix fun Float.greaterThan(num: Float): Boolean = this > num
infix fun Float.lessThanOrEqual(num: Float): Boolean = this <= num
infix fun Float.greaterThanOrEqual(num: Float): Boolean = this >= num